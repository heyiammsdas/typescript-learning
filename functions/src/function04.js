/**
 *
 * 8. Create an overloaded function joinValues(a, b):
If both are numbers → return sum
If both are strings → return concatenated string
 */
function joinValues(a, b) {
    return a + b;
}
var ans = joinValues(3, 4);
var ans2 = joinValues("Hell", " Love");
console.log(ans);
console.log(ans2);
